  /// as OrderBy/ThenBy chain. Supports dot-path tokens via DotPathResolver.Resolve.
  /// TASK05 §5.6.3. One-shot. Replaces per-controller inline ApplySorting helper.
  static String emitNsgSortingApplier(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    return '// <auto-generated>\n'
        '//   This file is generated by nsg_generator (one-shot — manual edits preserved on regen).\n'
        '// </auto-generated>\n'
        '#nullable enable\n'
        'using System;\n'
        'using System.Linq;\n'
        'using System.Linq.Expressions;\n'
        'using System.Reflection;\n'
        'using Microsoft.EntityFrameworkCore.Metadata;\n'
        'using $ns.Configurations;\n'
        '\n'
        'namespace $ns.Wire;\n'
        '\n'
        '/// <summary>Применяет wire Sorting к IQueryable&lt;T&gt; как OrderBy/ThenBy цепочку.\n'
        '/// TASK05 §5.6.3. Wire-формат: `<wireName><+/-?>` через запятые,\n'
        '/// например `"name+,shopId.photoId.name-"`. `+` (или отсутствие суффикса) = ascending,\n'

import 'dart:io';

import 'misc.dart';
import 'nsgGenDataItem.dart';
import 'nsgGenDataItemField.dart';
import 'nsgGenerator.dart';

/// Эмит netcore-проекта (EF Core + Postgres) — раунд 2 TASK04.
/// На текущем этапе: только Models/<T>.cs и Configurations/<T>Configuration.cs.
/// AppDbContext, csproj, Program.cs, appsettings.json — раунд 3.
///
/// Маппинг типов JSON → C# / Postgres — см. TASK04 §2.1.4.0.
/// Эталон — TASK02 §2.0.8 (ShopItem). Эмит сравнивается byte-identical.
class NsgGenNetcore {
  /// Колонка, по которой выравниваются inline-комментарии вида `// из pgColumnName`.
  static const int _commentColumn = 49;

  /// Эмитит Models/ + Configurations/ для одной сущности.
  static Future<void> emitDataItem(
      NsgGenerator gen, NsgGenDataItem di) async {
    final modelsDir = '${gen.netcoreOutputPath}/Models';
    final configsDir = '${gen.netcoreOutputPath}/Configurations';
    await Directory(modelsDir).create(recursive: true);
    await Directory(configsDir).create(recursive: true);

    final modelContent = emitModel(gen, di);
    await Misc.writeFileIfChanged(
        '$modelsDir/${di.typeName}.cs', modelContent);

    final configContent = emitConfiguration(gen, di);
    await Misc.writeFileIfChanged(
        '$configsDir/${di.typeName}Configuration.cs', configContent);
  }

  /// Эмит `Models/<T>.cs` — POCO EF entity.
  /// Public для unit-тестов.
  static String emitModel(NsgGenerator gen, NsgGenDataItem di) {
    final ns = gen.cSharpNamespace;
    final hasList =
        di.fields.any((f) => f.type.startsWith('List<') && _isEmittedField(f));
    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('// </auto-generated>');
    sb.writeln('using System;');
    if (hasList) {
      sb.writeln('using System.Collections.Generic;');
    }
    sb.writeln();
    sb.writeln('namespace $ns.Models;');
    sb.writeln();
    sb.writeln('public class ${di.typeName}');
    sb.writeln('{');

    // Группировка полей: PK / scalar / FK-pair / UntypedReference-pair / List.
    // Между группами — пустая строка; внутри FK/UntypedReference-пары — нет.
    final groups = <List<String>>[];

    for (final f in di.fields) {
      if (!_isEmittedField(f)) continue;

      if (f.isPrimary) {
        groups.add([
          '    public Guid ${_pkPropertyName(f)} { get; set; }',
        ]);
      } else if (_isReference(f)) {
        // FK pair: Guid? <name> + T? <referenceName>
        groups.add([
          '    public Guid? ${f.name} { get; set; }',
          '    public ${f.referenceType}? ${f.referenceName} { get; set; }',
        ]);
      } else if (_isUntypedReference(f)) {
        // UntypedReference pair: string? <referenceName>Type + Guid? <name>
        groups.add([
          '    public string? ${f.referenceName}Type { get; set; }',
          '    public Guid? ${f.name} { get; set; }',
        ]);
      } else if (_isList(f)) {
        // Nav-коллекция: ICollection<T> с инициализацией (решение B).
        groups.add([
          '    public ICollection<${f.referenceType}> ${f.name} { get; set; } = new List<${f.referenceType}>();',
        ]);
      } else {
        // Scalar
        groups.add([
          '    public ${_csScalarType(f)}? ${f.name} { get; set; }',
        ]);
      }
    }

    for (var i = 0; i < groups.length; i++) {
      for (final line in groups[i]) {
        sb.writeln(line);
      }
      if (i != groups.length - 1) sb.writeln();
    }
    sb.writeln('}');
    return sb.toString();
  }

  /// Эмит `Configurations/<T>Configuration.cs` — IEntityTypeConfiguration<T>.
  static String emitConfiguration(NsgGenerator gen, NsgGenDataItem di) {
    final ns = gen.cSharpNamespace;
    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('// </auto-generated>');
    sb.writeln('using Microsoft.EntityFrameworkCore;');
    sb.writeln('using Microsoft.EntityFrameworkCore.Metadata.Builders;');
    sb.writeln('using $ns.Models;');
    sb.writeln();
    sb.writeln('namespace $ns.Configurations;');
    sb.writeln();
    sb.writeln(
        'public class ${di.typeName}Configuration : IEntityTypeConfiguration<${di.typeName}>');
    sb.writeln('{');
    sb.writeln(
        '    public void Configure(EntityTypeBuilder<${di.typeName}> builder)');
    sb.writeln('    {');
    sb.writeln('        // Из pgTableName.');
    sb.writeln('        builder.ToTable("${di.pgTableName}");');
    sb.writeln();

    final pk = di.fields.firstWhere((f) => f.isPrimary,
        orElse: () => throw Exception(
            'netcore: no primary key on type "${di.typeName}"'));
    sb.writeln('        builder.HasKey(x => x.${_pkPropertyName(pk)});');

    final emittedFields = di.fields.where(_isEmittedField).toList();

    // Группы конфигураций per-field. Между группами — пустая строка.
    final blocks = <List<String>>[];

    for (final f in emittedFields) {
      if (f.isPrimary) {
        blocks.add(_configBlockForPk(f));
      } else if (_isReference(f)) {
        blocks.add(_configBlockForReference(f));
      } else if (_isUntypedReference(f)) {
        blocks.add(_configBlockForUntypedReference(f));
      } else if (_isList(f)) {
        // Owner-side: ничего не эмитим в Configurations; конфигурация на
        // стороне табчасти-row (HasOne(owner).WithMany(o => o.List)).
        continue;
      } else {
        blocks.add(_configBlockForScalar(f));
      }
    }

    for (var i = 0; i < blocks.length; i++) {
      // Между builder.HasKey и первым блоком пустой строки нет в эталоне —
      // первый блок (PK Property) идёт сразу следом.
      if (i > 0) sb.writeln();
      for (final line in blocks[i]) {
        sb.writeln(line);
      }
    }

    sb.writeln('    }');
    sb.writeln('}');
    return sb.toString();
  }

  // --- per-field helpers ---

  static List<String> _configBlockForPk(NsgGenDataItemField f) {
    // PK всегда Guid в netcore-эмите (решение №3).
    final propName = _pkPropertyName(f);
    final col = f.pgColumnName;
    return [
      '        builder.Property(x => x.$propName)',
      _alignInlineComment(
          '            .HasColumnName("$col")', '// из pgColumnName'),
      '            .HasColumnType("uuid");',
    ];
  }

  static List<String> _configBlockForScalar(NsgGenDataItemField f) {
    final col = f.pgColumnName;
    final pgType = _pgScalarColumnType(f);
    // `HasMaxLength` намеренно НЕ эмитится: в Postgres `text` безразмерный,
    // а `maxLength` в текущей схеме конфига сложно отличить от
    // NsgFramework-дефолта (10000 для String). См. TASK04 «Известные хвосты».
    final lines = <String>[];
    lines.add('        builder.Property(x => x.${f.name})');
    if (pgType != null) {
      lines.add(_alignInlineComment(
          '            .HasColumnName("$col")', '// из pgColumnName'));
      lines.add('            .HasColumnType("$pgType");');
    } else {
      lines.add(_alignInlineComment(
          '            .HasColumnName("$col");', '// из pgColumnName'));
    }
    return lines;
  }

  static List<String> _configBlockForReference(NsgGenDataItemField f) {
    final col = f.pgColumnName;
    // f.name уже содержит автоматический Id-суффикс (см. nsgGenDataItemField.dart:76-85)
    return [
      '        // FK: pgColumnName = "$col" + автоматический "_id" суффикс',
      '        // (симметрия с Dart-логикой nsgGenDataItemField.dart:76-85).',
      '        builder.Property(x => x.${f.name})',
      '            .HasColumnName("${col}_id")',
      '            .HasColumnType("uuid");',
      '',
      '        builder.HasOne(x => x.${f.referenceName})',
      '            .WithMany()',
      '            .HasForeignKey(x => x.${f.name})',
      '            .OnDelete(DeleteBehavior.Restrict);',
    ];
  }

  static List<String> _configBlockForUntypedReference(
      NsgGenDataItemField f) {
    final col = f.pgColumnName;
    // Имя свойства Type — <referenceName>Type. Id — f.name (уже с Id).
    final typeProp = '${f.referenceName}Type';
    return [
      '        // UntypedReference: пара колонок ${col}_type / ${col}_id, без FK',
      '        // (полиморфно). Составной индекс — решение №7 архитектуры.',
      '        builder.Property(x => x.$typeProp)',
      '            .HasColumnName("${col}_type")',
      '            .HasColumnType("text");',
      '',
      '        builder.Property(x => x.${f.name})',
      '            .HasColumnName("${col}_id")',
      '            .HasColumnType("uuid");',
      '',
      '        builder.HasIndex(x => new { x.$typeProp, x.${f.name} });',
    ];
  }

  // --- type predicates ---

  /// Поле попадает в EF entity, если у него есть `databaseName` (значит и
  /// `pgColumnName` в netcore-режиме). Поля без `databaseName` —
  /// write-only/derived, не маппятся в БД.
  /// Исключение: PK всегда эмитится, даже если `databaseName` пустое.
  static bool _isEmittedField(NsgGenDataItemField f) {
    return f.databaseName.isNotEmpty || f.isPrimary;
  }

  static bool _isReference(NsgGenDataItemField f) =>
      f.type.startsWith('Reference') && !f.type.startsWith('UntypedReference');

  static bool _isUntypedReference(NsgGenDataItemField f) =>
      f.type.startsWith('UntypedReference');

  static bool _isList(NsgGenDataItemField f) => f.type.startsWith('List<');

  /// Имя PK-property. По решению №3 — `Id` (тип `Guid`), вне зависимости
  /// от JSON `type: "String"` / `"Guid"`.
  static String _pkPropertyName(NsgGenDataItemField f) => f.name;

  /// Маппинг scalar non-PK типа JSON → C# (без `?` — суффикс добавляется
  /// в шаблоне).
  static String _csScalarType(NsgGenDataItemField f) {
    if (f.type == 'String' || f.type.startsWith('String<')) return 'string';
    if (f.type == 'int') return 'int';
    if (f.type == 'bool') return 'bool';
    if (f.type == 'double') return 'double';
    if (f.type == 'DateTime') return 'DateTime';
    if (f.type == 'Guid') return 'Guid';
    if (f.type == 'Image' || f.type == 'Binary') return 'byte[]';
    if (f.type.startsWith('Enum<') ||
        (f.type == 'Enum' && f.referenceType.isNotEmpty)) {
      return f.referenceType;
    }
    throw Exception(
        'netcore: unsupported scalar type "${f.type}" on field "${f.name}"');
  }

  /// Postgres-тип для `HasColumnType(...)`. `null` означает «не эмитить
  /// `HasColumnType`» (EF Core возьмёт по дефолту: integer/boolean/...).
  static String? _pgScalarColumnType(NsgGenDataItemField f) {
    if (f.type == 'Guid') return 'uuid';
    if (f.type == 'DateTime') {
      // useDate / useTime: оба default true → timestamptz.
      if (f.useDate && f.useTime) return 'timestamptz';
      if (f.useDate && !f.useTime) return 'date';
      if (!f.useDate && f.useTime) return 'time';
      throw Exception(
          'netcore: DateTime field "${f.name}" has useDate=false && useTime=false — invalid');
    }
    if (f.type == 'Image' || f.type == 'Binary') return 'bytea';
    // String/int/bool/double/Enum — EF Core знает по умолчанию, не эмитим
    // HasColumnType. (String → text без аргументов в Postgres-провайдере.)
    return null;
  }

  /// Выравнивает inline-комментарий по колонке `_commentColumn`.
  /// Если префикс длиннее — два пробела перед комментарием.
  static String _alignInlineComment(String prefix, String comment) {
    final padding = _commentColumn - prefix.length;
    final pad = padding > 0 ? ' ' * padding : '  ';
    return '$prefix$pad$comment';
  }
}

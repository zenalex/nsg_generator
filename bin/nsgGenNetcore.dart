import 'dart:io';

import 'misc.dart';
import 'nsgGenDataItem.dart';
import 'nsgGenDataItemField.dart';
import 'nsgGenerator.dart';

/// Эмит netcore-проекта (EF Core + Postgres) — раунды 2 + 3 TASK04.
/// Раунд 2: Models/<T>.cs, Configurations/<T>Configuration.cs.
/// Раунд 3.А: AppDbContext.Designer.cs (overwrite) + AppDbContext.cs (one-shot).
/// Раунд 3.Б: <name>.csproj, Program.cs, appsettings.json,
///            Properties/launchSettings.json (все one-shot).
///
/// Маппинг типов JSON → C# / Postgres — см. TASK04 §2.1.4.0.
/// Эталон — TASK02 §2.0.8 (ShopItem). Эмит сравнивается byte-identical.
class NsgGenNetcore {
  /// Колонка, по которой выравниваются inline-комментарии вида `// из pgColumnName`.
  static const int _commentColumn = 49;

  /// Версии NuGet-package-ов (TASK04 §«Версии package-ов»). При бампе —
  /// менять здесь и в `expected`-литералах соответствующих тестов.
  static const String efCoreVersion = '10.0.8';
  static const String npgsqlVersion = '10.0.1';

  /// TargetFramework (см. server-rewrite.md §1).
  static const String targetFramework = 'net10.0';

  /// Эмитит Models/ + Configurations/ для одной сущности.
  static Future<void> emitDataItem(
      NsgGenerator gen, NsgGenDataItem di) async {
    final modelsDir = '${gen.netcoreOutputPath}/Models';
    final configsDir = '${gen.netcoreOutputPath}/Configurations';
    await Directory(modelsDir).create(recursive: true);
    await Directory(configsDir).create(recursive: true);

    final modelContent = emitModel(gen, di);
    await Misc.writeFileIfChanged(
        '$modelsDir/${di.typeName}.cs', modelContent);

    final configContent = emitConfiguration(gen, di);
    await Misc.writeFileIfChanged(
        '$configsDir/${di.typeName}Configuration.cs', configContent);
  }

  /// Эмитит `<netcoreOutputPath>/AppDbContext.Designer.cs` (overwrite) и
  /// `AppDbContext.cs` (one-shot — повторный прогон не перезаписывает).
  /// Раунд 3.А TASK04.
  static Future<void> emitDbContext(NsgGenerator gen) async {
    await Directory(gen.netcoreOutputPath).create(recursive: true);

    final designer = emitDbContextDesigner(gen);
    await Misc.writeFileIfChanged(
        '${gen.netcoreOutputPath}/AppDbContext.Designer.cs', designer);

    final manualPath = '${gen.netcoreOutputPath}/AppDbContext.cs';
    final manualFile = File(manualPath);
    if (!await manualFile.exists() || gen.forceOverwrite) {
      await Misc.writeFileIfChanged(manualPath, emitDbContextManual(gen));
    }
  }

  /// Эмит `AppDbContext.Designer.cs` — generated-only часть partial-класса.
  /// Public для unit-тестов.
  static String emitDbContextDesigner(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    // Алфавитный порядок по typeName — детерминированный, устойчивый
    // к перестановке методов в generation_config.json.
    final sortedItems = gen.dataItems.values.toList()
      ..sort((a, b) => a.typeName.compareTo(b.typeName));

    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('// </auto-generated>');
    sb.writeln('#nullable enable');
    sb.writeln('using Microsoft.EntityFrameworkCore;');
    sb.writeln('using $ns.Configurations;');
    sb.writeln('using $ns.Models;');
    sb.writeln();
    sb.writeln('namespace $ns;');
    sb.writeln();
    sb.writeln('public partial class AppDbContext : DbContext');
    sb.writeln('{');
    for (final di in sortedItems) {
      sb.writeln(
          '    public DbSet<${di.typeName}> ${dbSetName(di.typeName)} => Set<${di.typeName}>();');
    }
    sb.writeln();
    sb.writeln(
        '    partial void OnModelCreatingGenerated(ModelBuilder modelBuilder)');
    sb.writeln('    {');
    for (final di in sortedItems) {
      sb.writeln(
          '        modelBuilder.ApplyConfiguration(new ${di.typeName}Configuration());');
    }
    sb.writeln('    }');
    sb.writeln('}');
    return sb.toString();
  }

  /// Эмит `AppDbContext.cs` — manual partial. One-shot: создаётся при отсутствии
  /// файла, иначе пропускается (логика one-shot — в `emitDbContext`).
  /// Public для unit-тестов.
  static String emitDbContextManual(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    final sb = StringBuffer();
    sb.writeln('using Microsoft.EntityFrameworkCore;');
    sb.writeln();
    sb.writeln('namespace $ns;');
    sb.writeln();
    sb.writeln('public partial class AppDbContext : DbContext');
    sb.writeln('{');
    sb.writeln(
        '    public AppDbContext(DbContextOptions<AppDbContext> options) : base(options) { }');
    sb.writeln();
    sb.writeln(
        '    protected override void OnModelCreating(ModelBuilder modelBuilder)');
    sb.writeln('    {');
    sb.writeln('        OnModelCreatingGenerated(modelBuilder);');
    sb.writeln('        // место для ручных конфигураций / seed');
    sb.writeln('    }');
    sb.writeln();
    sb.writeln(
        '    partial void OnModelCreatingGenerated(ModelBuilder modelBuilder);');
    sb.writeln('}');
    return sb.toString();
  }

  /// Эмит host-инфраструктуры (раунд 3.Б): csproj, Program.cs, appsettings.json,
  /// Properties/launchSettings.json. Все 4 файла — **one-shot**: создаются при
  /// первом эмите и не перезаписываются при повторном (если нет
  /// `forceOverwrite`). Версии package-ов — см. `efCoreVersion`/`npgsqlVersion`.
  static Future<void> emitHostingFiles(NsgGenerator gen) async {
    final root = gen.netcoreOutputPath;
    final propsDir = '$root/Properties';
    await Directory(root).create(recursive: true);
    await Directory(propsDir).create(recursive: true);

    await _writeOneShot(gen,
        path: '$root/${gen.cSharpNamespace}.csproj',
        content: emitCsproj(gen));
    await _writeOneShot(gen,
        path: '$root/Program.cs', content: emitProgramCs(gen));
    await _writeOneShot(gen,
        path: '$root/appsettings.json', content: emitAppsettings(gen));
    await _writeOneShot(gen,
        path: '$propsDir/launchSettings.json',
        content: emitLaunchSettings(gen));
  }

  static Future<void> _writeOneShot(NsgGenerator gen,
      {required String path, required String content}) async {
    final file = File(path);
    if (!await file.exists() || gen.forceOverwrite) {
      await Misc.writeFileIfChanged(path, content);
    }
  }

  /// Эмит `<cSharpNamespace>.csproj`. SDK = `Microsoft.NET.Sdk.Web`,
  /// без `<ImplicitUsings>` (явные using в Program.cs/Models/Configurations
  /// предсказуемее). Public для тестов.
  static String emitCsproj(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    return '<Project Sdk="Microsoft.NET.Sdk.Web">\n'
        '\n'
        '  <PropertyGroup>\n'
        '    <TargetFramework>$targetFramework</TargetFramework>\n'
        '    <Nullable>enable</Nullable>\n'
        '    <RootNamespace>$ns</RootNamespace>\n'
        '  </PropertyGroup>\n'
        '\n'
        '  <ItemGroup>\n'
        '    <PackageReference Include="Microsoft.EntityFrameworkCore" Version="$efCoreVersion" />\n'
        '    <PackageReference Include="Microsoft.EntityFrameworkCore.Design" Version="$efCoreVersion">\n'
        '      <PrivateAssets>all</PrivateAssets>\n'
        '      <IncludeAssets>runtime; build; native; contentfiles; analyzers; buildtransitive</IncludeAssets>\n'
        '    </PackageReference>\n'
        '    <PackageReference Include="Npgsql.EntityFrameworkCore.PostgreSQL" Version="$npgsqlVersion" />\n'
        '  </ItemGroup>\n'
        '\n'
        '</Project>\n';
  }

  /// Минимальный `Program.cs` — host ASP.NET Core с зарегистрированным
  /// `AppDbContext` поверх Npgsql. Контроллеры — подзадача 2.1.10.
  static String emitProgramCs(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    return 'using Microsoft.AspNetCore.Builder;\n'
        'using Microsoft.EntityFrameworkCore;\n'
        'using Microsoft.Extensions.Configuration;\n'
        'using Microsoft.Extensions.DependencyInjection;\n'
        'using $ns;\n'
        '\n'
        'var builder = WebApplication.CreateBuilder(args);\n'
        'builder.Services.AddDbContext<AppDbContext>(opt =>\n'
        '    opt.UseNpgsql(builder.Configuration.GetConnectionString("DefaultConnection")));\n'
        'var app = builder.Build();\n'
        'app.Run();\n';
  }

  /// Минимальный `appsettings.json`. Connection string — dev-значения с
  /// именем БД из `cSharpNamespace` (lowercase). Прод-секреты — out of scope.
  static String emitAppsettings(NsgGenerator gen) {
    final dbName = gen.cSharpNamespace.toLowerCase();
    return '{\n'
        '  "ConnectionStrings": {\n'
        '    "DefaultConnection": "Host=localhost;Port=5432;Database=$dbName;Username=postgres;Password=postgres"\n'
        '  },\n'
        '  "Logging": {\n'
        '    "LogLevel": {\n'
        '      "Default": "Information",\n'
        '      "Microsoft.AspNetCore": "Warning"\n'
        '    }\n'
        '  },\n'
        '  "AllowedHosts": "*"\n'
        '}\n';
  }

  /// `Properties/launchSettings.json` — один dev-профиль `http` на 5000.
  static String emitLaunchSettings(NsgGenerator gen) {
    return '{\n'
        '  "\$schema": "http://json.schemastore.org/launchsettings.json",\n'
        '  "profiles": {\n'
        '    "http": {\n'
        '      "commandName": "Project",\n'
        '      "dotnetRunMessages": true,\n'
        '      "launchBrowser": false,\n'
        '      "applicationUrl": "http://localhost:5000",\n'
        '      "environmentVariables": {\n'
        '        "ASPNETCORE_ENVIRONMENT": "Development"\n'
        '      }\n'
        '    }\n'
        '  }\n'
        '}\n';
  }

  /// Имя `DbSet<T>` свойства по правилу из TASK04 §2.1.4.0:
  /// `<TypeName>Item` → `<TypeName>s`; иначе `<TypeName>s`.
  /// Public для тестов.
  static String dbSetName(String typeName) {
    if (typeName.endsWith('Item')) {
      return '${typeName.substring(0, typeName.length - 4)}s';
    }
    return '${typeName}s';
  }

  /// Эмит `Models/<T>.cs` — POCO EF entity.
  /// Public для unit-тестов.
  static String emitModel(NsgGenerator gen, NsgGenDataItem di) {
    final ns = gen.cSharpNamespace;
    final hasList =
        di.fields.any((f) => f.type.startsWith('List<') && _isEmittedField(f));
    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('// </auto-generated>');
    // Шапка <auto-generated> отключает nullable-контекст в C#; явное
    // `#nullable enable` нужно, чтобы string?/Guid?/T? не давали CS8669
    // (TASK04 §«Известные хвосты» I, фикс 4.0).
    sb.writeln('#nullable enable');
    sb.writeln('using System;');
    if (hasList) {
      sb.writeln('using System.Collections.Generic;');
    }
    sb.writeln();
    sb.writeln('namespace $ns.Models;');
    sb.writeln();
    sb.writeln('public class ${di.typeName}');
    sb.writeln('{');

    // Группировка полей: PK / scalar / FK-pair / UntypedReference-pair / List.
    // Между группами — пустая строка; внутри FK/UntypedReference-пары — нет.
    final groups = <List<String>>[];

    for (final f in di.fields) {
      if (!_isEmittedField(f)) continue;

      if (f.isPrimary) {
        groups.add([
          '    public Guid ${_pkPropertyName(f)} { get; set; }',
        ]);
      } else if (_isReference(f)) {
        // FK pair: Guid? <name> + T? <referenceName>
        groups.add([
          '    public Guid? ${f.name} { get; set; }',
          '    public ${f.referenceType}? ${f.referenceName} { get; set; }',
        ]);
      } else if (_isUntypedReference(f)) {
        // UntypedReference pair: string? <referenceName>Type + Guid? <name>
        groups.add([
          '    public string? ${f.referenceName}Type { get; set; }',
          '    public Guid? ${f.name} { get; set; }',
        ]);
      } else if (_isList(f)) {
        // Nav-коллекция: ICollection<T> с инициализацией (решение B).
        groups.add([
          '    public ICollection<${f.referenceType}> ${f.name} { get; set; } = new List<${f.referenceType}>();',
        ]);
      } else {
        // Scalar
        groups.add([
          '    public ${_csScalarType(f)}? ${f.name} { get; set; }',
        ]);
      }
    }

    for (var i = 0; i < groups.length; i++) {
      for (final line in groups[i]) {
        sb.writeln(line);
      }
      if (i != groups.length - 1) sb.writeln();
    }
    sb.writeln('}');
    return sb.toString();
  }

  /// Map `<TableRowTypeName> → <OwnerTypeName>` для всех isTableRow-сущностей.
  /// Owner находится через поиск поля `List<<TableRowTypeName>>` или
  /// `List<Reference>` + `referenceType == TableRowTypeName` в других сущностях.
  /// Используется в `_configBlockForReference` для эмита `OnDelete(Cascade)`
  /// вместо `Restrict` (архитектурное решение №6).
  static Map<String, String> computeTableRowOwners(NsgGenerator gen) {
    final result = <String, String>{};
    for (final tableRow in gen.dataItems.values) {
      if (!tableRow.isTableRow) continue;
      for (final owner in gen.dataItems.values) {
        if (owner.typeName == tableRow.typeName) continue;
        final hit = owner.fields.any((f) =>
            f.type.startsWith('List<') &&
            f.referenceType == tableRow.typeName);
        if (hit) {
          result[tableRow.typeName] = owner.typeName;
          break;
        }
      }
    }
    return result;
  }

  /// Эмит `Configurations/<T>Configuration.cs` — IEntityTypeConfiguration<T>.
  static String emitConfiguration(NsgGenerator gen, NsgGenDataItem di) {
    final ns = gen.cSharpNamespace;
    // Если `di` — табчасть-row, ownerType указывает на тип-владельца.
    // FK на этот тип в `di` получит `OnDelete(Cascade)` (решение №6).
    final ownerType = computeTableRowOwners(gen)[di.typeName];
    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('// </auto-generated>');
    sb.writeln('#nullable enable');
    sb.writeln('using Microsoft.EntityFrameworkCore;');
    sb.writeln('using Microsoft.EntityFrameworkCore.Metadata.Builders;');
    sb.writeln('using $ns.Models;');
    sb.writeln();
    sb.writeln('namespace $ns.Configurations;');
    sb.writeln();
    sb.writeln(
        'public class ${di.typeName}Configuration : IEntityTypeConfiguration<${di.typeName}>');
    sb.writeln('{');
    sb.writeln(
        '    public void Configure(EntityTypeBuilder<${di.typeName}> builder)');
    sb.writeln('    {');
    sb.writeln('        // Из pgTableName.');
    sb.writeln('        builder.ToTable("${di.pgTableName}");');
    sb.writeln();

    final pk = di.fields.firstWhere((f) => f.isPrimary,
        orElse: () => throw Exception(
            'netcore: no primary key on type "${di.typeName}"'));
    sb.writeln('        builder.HasKey(x => x.${_pkPropertyName(pk)});');

    final emittedFields = di.fields.where(_isEmittedField).toList();

    // Группы конфигураций per-field. Между группами — пустая строка.
    final blocks = <List<String>>[];

    for (final f in emittedFields) {
      if (f.isPrimary) {
        blocks.add(_configBlockForPk(f));
      } else if (_isReference(f)) {
        final isOwnerFk =
            ownerType != null && f.referenceType == ownerType;
        blocks.add(_configBlockForReference(f, isOwnerFk: isOwnerFk));
      } else if (_isUntypedReference(f)) {
        blocks.add(_configBlockForUntypedReference(f));
      } else if (_isList(f)) {
        // Owner-side: ничего не эмитим в Configurations; конфигурация на
        // стороне табчасти-row (HasOne(owner).WithMany(o => o.List)).
        continue;
      } else {
        blocks.add(_configBlockForScalar(f));
      }
    }

    for (var i = 0; i < blocks.length; i++) {
      // Между builder.HasKey и первым блоком пустой строки нет в эталоне —
      // первый блок (PK Property) идёт сразу следом.
      if (i > 0) sb.writeln();
      for (final line in blocks[i]) {
        sb.writeln(line);
      }
    }

    sb.writeln('    }');
    sb.writeln('}');
    return sb.toString();
  }

  // --- per-field helpers ---

  static List<String> _configBlockForPk(NsgGenDataItemField f) {
    // PK всегда Guid в netcore-эмите (решение №3).
    final propName = _pkPropertyName(f);
    final col = f.pgColumnName;
    return [
      '        builder.Property(x => x.$propName)',
      _alignInlineComment(
          '            .HasColumnName("$col")', '// из pgColumnName'),
      '            .HasColumnType("uuid");',
    ];
  }

  static List<String> _configBlockForScalar(NsgGenDataItemField f) {
    final col = f.pgColumnName;
    final pgType = _pgScalarColumnType(f);
    // `HasMaxLength` намеренно НЕ эмитится: в Postgres `text` безразмерный,
    // а `maxLength` в текущей схеме конфига сложно отличить от
    // NsgFramework-дефолта (10000 для String). См. TASK04 «Известные хвосты».
    final lines = <String>[];
    lines.add('        builder.Property(x => x.${f.name})');
    if (pgType != null) {
      lines.add(_alignInlineComment(
          '            .HasColumnName("$col")', '// из pgColumnName'));
      lines.add('            .HasColumnType("$pgType");');
    } else {
      lines.add(_alignInlineComment(
          '            .HasColumnName("$col");', '// из pgColumnName'));
    }
    return lines;
  }

  static List<String> _configBlockForReference(NsgGenDataItemField f,
      {bool isOwnerFk = false}) {
    final col = f.pgColumnName;
    // f.name уже содержит автоматический Id-суффикс (см. nsgGenDataItemField.dart:76-85)
    final deleteBehavior = isOwnerFk ? 'Cascade' : 'Restrict';
    final ownerComment = isOwnerFk
        ? [
            '        // FK табчасть → owner: Cascade (решение №6 архитектуры).',
            '        // Owner найден автоматически: ${f.referenceType} содержит List<этой сущности>.',
          ]
        : <String>[];
    return [
      ...ownerComment,
      '        // FK: pgColumnName = "$col" + автоматический "_id" суффикс',
      '        // (симметрия с Dart-логикой nsgGenDataItemField.dart:76-85).',
      '        builder.Property(x => x.${f.name})',
      '            .HasColumnName("${col}_id")',
      '            .HasColumnType("uuid");',
      '',
      '        builder.HasOne(x => x.${f.referenceName})',
      '            .WithMany()',
      '            .HasForeignKey(x => x.${f.name})',
      '            .OnDelete(DeleteBehavior.$deleteBehavior);',
    ];
  }

  static List<String> _configBlockForUntypedReference(
      NsgGenDataItemField f) {
    final col = f.pgColumnName;
    // Имя свойства Type — <referenceName>Type. Id — f.name (уже с Id).
    final typeProp = '${f.referenceName}Type';
    return [
      '        // UntypedReference: пара колонок ${col}_type / ${col}_id, без FK',
      '        // (полиморфно). Составной индекс — решение №7 архитектуры.',
      '        builder.Property(x => x.$typeProp)',
      '            .HasColumnName("${col}_type")',
      '            .HasColumnType("text");',
      '',
      '        builder.Property(x => x.${f.name})',
      '            .HasColumnName("${col}_id")',
      '            .HasColumnType("uuid");',
      '',
      '        builder.HasIndex(x => new { x.$typeProp, x.${f.name} });',
    ];
  }

  // --- type predicates ---

  /// Поле попадает в EF entity, если у него есть `databaseName` (значит и
  /// `pgColumnName` в netcore-режиме). Поля без `databaseName` —
  /// write-only/derived, не маппятся в БД.
  /// Исключение: PK всегда эмитится, даже если `databaseName` пустое.
  static bool _isEmittedField(NsgGenDataItemField f) {
    return f.databaseName.isNotEmpty || f.isPrimary;
  }

  static bool _isReference(NsgGenDataItemField f) =>
      f.type.startsWith('Reference') && !f.type.startsWith('UntypedReference');

  static bool _isUntypedReference(NsgGenDataItemField f) =>
      f.type.startsWith('UntypedReference');

  static bool _isList(NsgGenDataItemField f) => f.type.startsWith('List<');

  /// Имя PK-property. По решению №3 — `Id` (тип `Guid`), вне зависимости
  /// от JSON `type: "String"` / `"Guid"`.
  static String _pkPropertyName(NsgGenDataItemField f) => f.name;

  /// Маппинг scalar non-PK типа JSON → C# (без `?` — суффикс добавляется
  /// в шаблоне).
  static String _csScalarType(NsgGenDataItemField f) {
    if (f.type == 'String' || f.type.startsWith('String<')) return 'string';
    if (f.type == 'int') return 'int';
    if (f.type == 'bool') return 'bool';
    if (f.type == 'double') return 'double';
    if (f.type == 'DateTime') return 'DateTime';
    if (f.type == 'Guid') return 'Guid';
    if (f.type == 'Image' || f.type == 'Binary') return 'byte[]';
    if (f.type.startsWith('Enum<') ||
        (f.type == 'Enum' && f.referenceType.isNotEmpty)) {
      return f.referenceType;
    }
    throw Exception(
        'netcore: unsupported scalar type "${f.type}" on field "${f.name}"');
  }

  /// Postgres-тип для `HasColumnType(...)`. `null` означает «не эмитить
  /// `HasColumnType`» (EF Core возьмёт по дефолту: integer/boolean/...).
  static String? _pgScalarColumnType(NsgGenDataItemField f) {
    if (f.type == 'Guid') return 'uuid';
    if (f.type == 'DateTime') {
      // useDate / useTime: оба default true → timestamptz.
      if (f.useDate && f.useTime) return 'timestamptz';
      if (f.useDate && !f.useTime) return 'date';
      if (!f.useDate && f.useTime) return 'time';
      throw Exception(
          'netcore: DateTime field "${f.name}" has useDate=false && useTime=false — invalid');
    }
    if (f.type == 'Image' || f.type == 'Binary') return 'bytea';
    // String/int/bool/double/Enum — EF Core знает по умолчанию, не эмитим
    // HasColumnType. (String → text без аргументов в Postgres-провайдере.)
    return null;
  }

  /// Выравнивает inline-комментарий по колонке `_commentColumn`.
  /// Если префикс длиннее — два пробела перед комментарием.
  static String _alignInlineComment(String prefix, String comment) {
    final padding = _commentColumn - prefix.length;
    final pad = padding > 0 ? ' ' * padding : '  ';
    return '$prefix$pad$comment';
  }
}

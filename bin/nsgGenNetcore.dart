    sb.writeln('        new Dictionary<string, string>');
    sb.writeln('        {');
    for (final entry in entries) {
      sb.writeln('            ["${entry.$2}"] = "${entry.$1}",');
    }
    sb.writeln('        };');
    sb.writeln('}');
    return sb.toString();
  }

  /// One-shot эмит глобального TypeNameMap. Записывается через

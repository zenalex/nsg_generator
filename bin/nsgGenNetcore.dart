  /// Эмитит Controllers/<T>Controller.cs (overwrite, partial) + Controllers/<T>Controller.Custom.cs
  /// (one-shot, partial-stub для бизнес-логики).
  static Future<void> emitControllerForMethod(
      NsgGenerator gen, NsgGenController controller, NsgGenMethod method) async {
    final controllersDir = '${gen.netcoreOutputPath}/Controllers';
    await Directory(controllersDir).create(recursive: true);
    final di = method.genDataItem;
    final dsName = dbSetName(di.typeName);
    final generatedContent = emitControllerGenerated(gen, controller, method, dsName);
    await Misc.writeFileIfChanged(
        '$controllersDir/${di.typeName}Controller.cs', generatedContent);
    final customPath = '$controllersDir/${di.typeName}Controller.Custom.cs';
    await _writeOneShot(gen,
        path: customPath,
        content: emitControllerCustom(gen, di));
  }

  /// Generated half of <T>Controller. Public для unit-тестов.
  static String emitControllerGenerated(NsgGenerator gen,
      NsgGenController controller, NsgGenMethod method, String dsName) {
    final ns = gen.cSharpNamespace;
    final di = method.genDataItem;
    final t = di.typeName;
    final route = '${controller.apiPrefix}/${method.apiPrefix}';

    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('//   For custom business logic edit $t' 'Controller.Custom.cs.');
    sb.writeln('// </auto-generated>');
    sb.writeln('#nullable enable');
    sb.writeln('using System;');
    sb.writeln('using System.Collections.Generic;');
    sb.writeln('using System.Linq;');

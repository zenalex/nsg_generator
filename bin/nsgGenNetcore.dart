  /// `Wire/NsgWireDeserializer.cs` — парсит входящий JSON в NsgRequestDto.
  /// Гибкий: принимает числа как string или number, bool как "true"/"false"
  /// или native true/false. Это нужно из-за клиентского `jsonEncode`,
  /// который оборачивает примитивы в строки.
  static String emitNsgWireDeserializer(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    return '// <auto-generated>\n'
        '//   This file is generated by nsg_generator (one-shot — manual edits preserved on regen).\n'
        '// </auto-generated>\n'
        '#nullable enable\n'
        'using System;\n'
        'using System.Collections.Generic;\n'
        'using System.Text.Json;\n'
        '\n'
        'namespace $ns.Wire;\n'
        '\n'
        '/// <summary>Парсит входящий JSON в <see cref="NsgRequestDto"/>.\n'
        '/// Толерантен к смешанной типизации wire (Top как "5" или 5).</summary>\n'
        'public static class NsgWireDeserializer\n'
        '{\n'
        '    public static NsgRequestDto ParseRequest(JsonElement root)\n'
        '    {\n'
        '        return new NsgRequestDto(\n'
        '            Top:                   TryGetInt(root, "Top"),\n'
        '            Count:                 TryGetInt(root, "Count"),\n'
        '            Sorting:               TryGetString(root, "Sorting"),\n'
        '            ReadReferences:        TryGetStringArray(root, "ReadReferences"),\n'
        '            NeededFields:          TryGetStringArray(root, "NeededFields"),\n'
        '            GroupBy:               TryGetStringArray(root, "GroupBy"),\n'
        '            Compare:               TryGetCompare(root, "Compare"),\n'
        '            ShowDeletedObjects:    TryGetBoolFromString(root, "ShowDeletedObjects") ?? false,\n'
        '            TransactionId:         TryGetString(root, "TransactionId"),\n'
        '            RequestId:             TryGetString(root, "RequestId"),\n'
        '            Parameters:            TryGetDictionary(root, "Parameters")\n'
        '        );\n'
        '    }\n'
        '\n'
        '    public static NsgCompareDto ParseCompare(JsonElement compareEl)\n'
        '    {\n'
        '        var logical = compareEl.TryGetProperty("LogicalOperator", out var lo) && lo.ValueKind == JsonValueKind.Number\n'
        '            ? lo.GetInt32()\n'
        '            : 1;\n'
        '        var list = new List<NsgCompareParamDto>();\n'
        '        if (compareEl.TryGetProperty("ParamList", out var pl) && pl.ValueKind == JsonValueKind.Array)\n'
        '        {\n'
        '            foreach (var p in pl.EnumerateArray())\n'
        '            {\n'
        '                var name = p.TryGetProperty("Name", out var n) && n.ValueKind == JsonValueKind.String ? n.GetString() ?? "" : "";\n'
        '                var op   = p.TryGetProperty("ComparisonOperator", out var o) && o.ValueKind == JsonValueKind.Number ? o.GetInt32() : 0;\n'
        '                var val  = p.TryGetProperty("Value", out var v) ? v.Clone() : default;\n'
        '                list.Add(new NsgCompareParamDto(name, op, val));\n'
        '            }\n'
        '        }\n'
        '        return new NsgCompareDto(logical, list.ToArray());\n'
        '    }\n'
        '\n'
        '    // -------- helpers --------\n'
        '\n'
        '    private static int? TryGetInt(JsonElement parent, string key)\n'
        '    {\n'
        '        if (!parent.TryGetProperty(key, out var v)) return null;\n'
        '        return v.ValueKind switch\n'
        '        {\n'
        '            JsonValueKind.Number => v.GetInt32(),\n'
        '            JsonValueKind.String => int.TryParse(v.GetString(), out var i) ? i : (int?)null,\n'
        '            _ => null,\n'
        '        };\n'
        '    }\n'
        '\n'
        '    private static string? TryGetString(JsonElement parent, string key)\n'
        '    {\n'
        '        if (!parent.TryGetProperty(key, out var v)) return null;\n'
        '        return v.ValueKind switch\n'
        '        {\n'
        '            JsonValueKind.String => v.GetString(),\n'
        '            JsonValueKind.Null   => null,\n'
        '            _ => v.GetRawText().Trim(\'"\'),  // jsonEncode-wrapped value\n'
        '        };\n'
        '    }\n'
        '\n'
        '    private static bool? TryGetBoolFromString(JsonElement parent, string key)\n'
        '    {\n'
        '        if (!parent.TryGetProperty(key, out var v)) return null;\n'
        '        return v.ValueKind switch\n'
        '        {\n'
        '            JsonValueKind.True  => true,\n'
        '            JsonValueKind.False => false,\n'
        '            JsonValueKind.String => v.GetString()?.ToLowerInvariant() switch\n'
        '            {\n'
        '                "true"  => true,\n'
        '                "false" => false,\n'
        '                _ => (bool?)null,\n'
        '            },\n'
        '            _ => null,\n'
        '        };\n'
        '    }\n'
        '\n'
        '    private static string[]? TryGetStringArray(JsonElement parent, string key)\n'
        '    {\n'
        '        if (!parent.TryGetProperty(key, out var v) || v.ValueKind != JsonValueKind.Array) return null;\n'
        '        var list = new List<string>();\n'
        '        foreach (var item in v.EnumerateArray())\n'
        '        {\n'
        '            if (item.ValueKind == JsonValueKind.String) list.Add(item.GetString() ?? "");\n'
        '        }\n'
        '        return list.ToArray();\n'
        '    }\n'
        '\n'
        '    private static NsgCompareDto? TryGetCompare(JsonElement parent, string key)\n'
        '    {\n'
        '        if (!parent.TryGetProperty(key, out var v) || v.ValueKind != JsonValueKind.Object) return null;\n'
        '        return ParseCompare(v);\n'
        '    }\n'
        '\n'
        '    private static Dictionary<string, JsonElement>? TryGetDictionary(JsonElement parent, string key)\n'
        '    {\n'
        '        if (!parent.TryGetProperty(key, out var v) || v.ValueKind != JsonValueKind.Object) return null;\n'
        '        var dict = new Dictionary<string, JsonElement>();\n'
        '        foreach (var p in v.EnumerateObject())\n'
        '        {\n'
        '            dict[p.Name] = p.Value.Clone();\n'
        '        }\n'
        '        return dict;\n'
        '    }\n'
        '}\n';
  }

  /// `Wire/NsgWireSerializer.cs` — EF entity → JSON через FieldMap.
  /// DateTime → ISO8601 UTC, Guid → string, enum → integer.
  /// Имена свойств транслируются через `<T>FieldMap.CSharpToWire`
  /// (резолвится через `TypeFieldMapRegistry.ByType[entityType]`).
  static String emitNsgWireSerializer(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    return '// <auto-generated>\n'
        '//   This file is generated by nsg_generator (one-shot — manual edits preserved on regen).\n'
        '// </auto-generated>\n'
        '#nullable enable\n'
        'using System;\n'
        'using System.Collections;\n'
        'using System.Collections.Generic;\n'
        'using System.Linq;\n'
        'using System.Text.Json;\n'
        'using $ns.Configurations;\n'
        '\n'
        'namespace $ns.Wire;\n'
        '\n'
        '/// <summary>Сериализует EF entity в wire JSON (camelCase wire-имена).\n'
        '/// Имена резолвятся через TypeFieldMapRegistry.ByType[entityType].</summary>\n'
        'public static class NsgWireSerializer\n'
        '{\n'
        '    public static Dictionary<string, object?> ToWireDict(object entity)\n'
        '    {\n'
        '        var type = entity.GetType();\n'
        '        if (!TypeFieldMapRegistry.ByType.TryGetValue(type, out var maps))\n'
        '            throw new InvalidOperationException(\$"No FieldMap registered for type {type.Name}");\n'
        '\n'
        '        var dict = new Dictionary<string, object?>(maps.CSharpToWire.Count);\n'
        '        foreach (var (csName, wireName) in maps.CSharpToWire)\n'
        '        {\n'
        '            var prop = type.GetProperty(csName)\n'
        '                ?? throw new InvalidOperationException(\$"C# property {csName} not found on {type.Name}");\n'
        '            var raw = prop.GetValue(entity);\n'
        '            dict[wireName] = ConvertForWire(raw);\n'
        '        }\n'
        '        return dict;\n'
        '    }\n'
        '\n'
        '    public static string ToWireJson(object entity, JsonSerializerOptions? opts = null)\n'
        '    {\n'
        '        return JsonSerializer.Serialize(ToWireDict(entity), opts);\n'
        '    }\n'
        '\n'
        '    private static object? ConvertForWire(object? raw)\n'
        '    {\n'
        '        if (raw is null) return null;\n'
        '        return raw switch\n'
        '        {\n'
        '            DateTime dt => dt.Kind == DateTimeKind.Utc ? dt : dt.ToUniversalTime(),\n'
        '            Guid g     => g.ToString("D"),\n'
        '            Enum e     => Convert.ToInt32(e),  // enum → int (decision №4 of table 2.1.4.0)\n'
        '            _          => raw,\n'
        '        };\n'
        '    }\n'
        '}\n';
  }


        'using System.Linq;\n'
        'using System.Net;\n'
        'using System.Net.Http;\n'
        'using System.Net.Http.Json;\n'
        'using System.Threading.Tasks;\n'
        'using Microsoft.AspNetCore.Hosting;\n'
        'using Microsoft.AspNetCore.Mvc.Testing;\n'
        'using Microsoft.AspNetCore.TestHost;\n'
        'using Microsoft.EntityFrameworkCore;\n'
        'using Microsoft.Extensions.DependencyInjection;\n'
        'using $ns;\n'
        'using Xunit;\n'
        '\n'
        'namespace $ns.Tests;\n'
        '\n'
        'public class InMemoryFactory : WebApplicationFactory<Program>\n'
        '{\n'
        '    protected override void ConfigureWebHost(IWebHostBuilder builder)\n'
        '    {\n'
        '        builder.ConfigureTestServices(services =>\n'
        '        {\n'
        '            // Remove all EF Core registrations from Program.cs Npgsql config —\n'
        '            // both DbContextOptions and the internal provider service.\n'
        '            var toRemove = services.Where(d =>\n'

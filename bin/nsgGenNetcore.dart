  static String emitTestBasicSmoke(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    // Find first method that allows getter — endpoint for smoke.
    String firstEndpoint = 'Api/Something';
    for (final c in gen.controllers) {
      for (final m in c.methods) {
        if (m.allowGetter) {
          firstEndpoint = '${c.apiPrefix}/${m.apiPrefix}';
          break;
        }
      }
      if (firstEndpoint != 'Api/Something') break;
    }

    return '// One-shot skeleton — generator does NOT overwrite this file on regen.\n'
        '// Smoke: WebApplicationFactory<Program> over EFCore.InMemory.\n'
        '// (InMemory вместо Sqlite — упрощение setup; FK-валидация не нужна для smoke.)\n'

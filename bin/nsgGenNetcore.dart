  /// Public для unit-тестов.
  static String emitEnumContent(NsgGenerator gen, NsgGenEnum e) {
    final ns = gen.cSharpNamespace;
    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('// </auto-generated>');
    sb.writeln('#nullable enable');
    sb.writeln();
    sb.writeln('namespace $ns.Models;');
    sb.writeln();
    sb.writeln('public enum ${e.className}');
    sb.writeln('{');
    final values = e.values ?? const <NsgGenEnumItem>[];
    for (var i = 0; i < values.length; i++) {
      final v = values[i];
      final raw = v.value;
      // value может быть int или String — оба попадают в enum-литерал как int.
      final intVal = raw is int ? raw : int.parse(raw.toString());

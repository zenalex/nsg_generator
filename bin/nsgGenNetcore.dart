  /// Эмитит Models/ + Configurations/ для одной сущности.
  /// Включает FieldMap (per-entity, решение A + G2 в TASK05 §5.0).
  static Future<void> emitDataItem(
      NsgGenerator gen, NsgGenDataItem di) async {
    final modelsDir = '${gen.netcoreOutputPath}/Models';
    final configsDir = '${gen.netcoreOutputPath}/Configurations';
    await Directory(modelsDir).create(recursive: true);
    await Directory(configsDir).create(recursive: true);

    final modelContent = emitModel(gen, di);
    await Misc.writeFileIfChanged(
        '$modelsDir/${di.typeName}.cs', modelContent);

    final configContent = emitConfiguration(gen, di);
    await Misc.writeFileIfChanged(
        '$configsDir/${di.typeName}Configuration.cs', configContent);

    final fieldMapContent = emitFieldMap(gen, di);
    await Misc.writeFileIfChanged(
        '$configsDir/${di.typeName}FieldMap.cs', fieldMapContent);
  }

  /// Эмит `Configurations/<T>FieldMap.cs` (per-entity, overwrite). Содержит два
  /// IReadOnlyDictionary: `WireToCSharp` и `CSharpToWire`. Покрывает только
  /// поля с непустым `databaseName` (т.е. реальные БД-колонки); навигационные
  /// `List<>`-коллекции и поля без `databaseName` исключены — у них нет wire-имени
  /// в формате `<dartName>`.
  ///
  /// Wire-имя поля = `dartName` (**camelCase** через `Misc.getDartName`,
  /// см. misc.dart:82); для FK поле уже имеет `name = <ref>Id`,
  /// dartName = `<ref>Id` в camelCase (например `PhotoId` → `photoId`).
  /// Это **отличается** от SQL-имени колонки (snake_case `photo_id`) —
  /// FieldMap не покрывает SQL-имена, только wire ↔ C# (см. TASK05 §5.0.3).
  /// Public для unit-тестов.
  static String emitFieldMap(NsgGenerator gen, NsgGenDataItem di) {
    final ns = gen.cSharpNamespace;
    final wireToCs = <(String wire, String cs)>[];
    for (final f in di.fields) {
      if (f.databaseName.isEmpty) continue;
      if (f.type.startsWith('List<')) continue;
      wireToCs.add((Misc.getDartName(f.name), f.name));
    }
    wireToCs.sort((a, b) => a.$1.compareTo(b.$1));

    final sb = StringBuffer();
    sb.writeln('// <auto-generated>');
    sb.writeln(
        '//   This file is generated by nsg_generator. Manual edits will be overwritten.');
    sb.writeln('// </auto-generated>');
    sb.writeln('#nullable enable');
    sb.writeln('using System.Collections.Generic;');
    sb.writeln();
    sb.writeln('namespace $ns.Configurations;');
    sb.writeln();
    sb.writeln('/// <summary>');
    sb.writeln(
        '/// Wire ↔ C# field name map for <see cref="$ns.Models.${di.typeName}"/>.');
    sb.writeln(
        '/// Wire keys are camelCase (dartName via Misc.getDartName); C# values are');
    sb.writeln(
        '/// PascalCase (entity property name). Note: wire-name ≠ SQL column name —');
    sb.writeln(
        '/// SQL uses snake_case via HasColumnName in Configurations (see TASK05 §5.0.3).');
    sb.writeln('/// </summary>');
    sb.writeln('public static class ${di.typeName}FieldMap');
    sb.writeln('{');
    sb.writeln(
        '    public static readonly IReadOnlyDictionary<string, string> WireToCSharp =');
    sb.writeln('        new Dictionary<string, string>');
    sb.writeln('        {');
    for (final entry in wireToCs) {
      sb.writeln('            ["${entry.$1}"] = "${entry.$2}",');
    }
    sb.writeln('        };');
    sb.writeln();
    sb.writeln(
        '    public static readonly IReadOnlyDictionary<string, string> CSharpToWire =');
    sb.writeln('        new Dictionary<string, string>');
    sb.writeln('        {');
    for (final entry in wireToCs) {
      sb.writeln('            ["${entry.$2}"] = "${entry.$1}",');
    }
    sb.writeln('        };');
    sb.writeln('}');
    return sb.toString();
  }

  /// Эмит глобального `Configurations/_TypeNameMap.cs` (один на проект,

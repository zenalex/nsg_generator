  /// `Wire/DotPathResolver.cs` — резолвер wire dot-path в chained Expression.Property.
  /// TASK05 §5.6.1.
  /// Wire-формат (см. §5.6.0): camelCase + Id-суффикс на FK-сегментах
  /// (e.g. "shopId.photoId.name" → `x.Shop.Photo.Name`).
  /// Алгоритм: split → для каждого intermediate-сегмента wire→C# FK через FieldMap,
  /// затем nav-property через EF Core IModel (primary) или strip-Id fallback;
  /// для leaf wire→C# property напрямую. Collection navigation (List<T>) НЕ
  /// поддерживается ни в leaf, ни в non-leaf позиции (хвост Z).
  /// One-shot — статический код, не зависит от схемы.
  static String emitDotPathResolver(NsgGenerator gen) {
    final ns = gen.cSharpNamespace;
    return '// <auto-generated>\n'
        '//   This file is generated by nsg_generator (one-shot — manual edits preserved on regen).\n'
        '// </auto-generated>\n'
        '#nullable enable\n'
        'using System;\n'
        'using System.Collections;\n'
        'using System.Collections.Generic;\n'
        'using System.Linq;\n'
        'using System.Linq.Expressions;\n'
        'using System.Reflection;\n'
        'using System.Text;\n'
        'using Microsoft.EntityFrameworkCore.Metadata;\n'
        'using $ns.Configurations;\n'
        '\n'
        'namespace $ns.Wire;\n'
        '\n'
        '/// <summary>Резолвит wire dot-path (camelCase + Id-суффикс на FK-сегментах,\n'
        '/// см. §5.6.0) в chained Expression.Property для построения LINQ-предиката.\n'
        '/// Используется как из NsgCompareTranslator (filter), так и из ApplySorting\n'
        '/// (sort dot-path), и из ReadReferences-handler (nav-path для Include).</summary>\n'
        'public static class DotPathResolver\n'
        '{\n'
        '    /// <summary>Резолвит filter/sort dot-path в (Expression member, Type leafType, Expression? nullGuard).\n'
        '    /// nullGuard ≠ null только если есть intermediate nav-segments — это AND-цепочка\n'
        '    /// `x.NavA != null && x.NavA.NavB != null && ...`, которую caller (BuildAtom) оборачивает\n'
        '    /// вокруг predicate-а. Без guard-а InMemory LINQ кидает NRE на `x.Nav.Field` когда Nav=null;\n'
        '    /// EF Core+Postgres сам делает LEFT JOIN с NULL-семантикой, но мы должны быть\n'
        '    /// провайдер-нейтральны.</summary>\n'
        '    public static (Expression Member, Type LeafType, Expression? NullGuard) Resolve(\n'
        '        ParameterExpression rootParam,\n'
        '        Type rootEntityType,\n'
        '        string wireDotPath,\n'
        '        IModel efModel)\n'
        '    {\n'
        '        var segments = wireDotPath.Split(\'.\');\n'
        '        if (segments.Length == 0 || segments.Any(string.IsNullOrEmpty))\n'
        '            throw new InvalidOperationException(\$"Invalid wire dot-path \\"{wireDotPath}\\": empty segments");\n'
        '\n'
        '        Expression current = rootParam;\n'
        '        Type currentType = rootEntityType;\n'
        '        var intermediateNavs = new List<Expression>();  // expressions для null-check каждого non-leaf nav\n'
        '\n'
        '        for (int i = 0; i < segments.Length; i++)\n'
        '        {\n'
        '            var seg = segments[i];\n'
        '            var isLast = i == segments.Length - 1;\n'
        '\n'
        '            if (!TypeFieldMapRegistry.ByType.TryGetValue(currentType, out var maps))\n'
        '                throw new InvalidOperationException(\$"No FieldMap registered for type {currentType.Name} (path \\"{wireDotPath}\\" segment \\"{seg}\\")");\n'
        '            if (!maps.WireToCSharp.TryGetValue(seg, out var csName))\n'
        '                throw new InvalidOperationException(\$"Wire segment \\"{seg}\\" not in {currentType.Name}.FieldMap (full path \\"{wireDotPath}\\")");\n'
        '\n'
        '            var prop = currentType.GetProperty(csName)\n'
        '                ?? throw new InvalidOperationException(\$"C# property {csName} not found on {currentType.Name} (path \\"{wireDotPath}\\")");\n'
        '\n'
        '            // Collection-leaf — Any/All семантика не реализована (хвост Z).\n'
        '            if (isLast && IsCollectionType(prop.PropertyType))\n'
        '                throw new NotImplementedException(\$"dot-path with collection leaf: \\"{wireDotPath}\\" — use Any-operator instead (out of scope for 5.6)");\n'
        '\n'
        '            if (isLast)\n'
        '            {\n'
        '                // Терминальный сегмент — может быть scalar (Name) или FK (PhotoId).\n'
        '                // Оба валидны как leaf для filter/sort.\n'
        '                current = Expression.Property(current, prop);\n'
        '                return (current, prop.PropertyType, BuildNullGuard(intermediateNavs));\n'
        '            }\n'
        '\n'
        '            // Non-leaf: ожидаем FK + nav-property. Резолвим nav через EF Core IModel\n'
        '            // (primary: matching по INavigation.ForeignKey.Properties[0].Name == csName).\n'
        '            var navProp = ResolveNavProperty(currentType, csName, efModel, wireDotPath, seg);\n'
        '\n'
        '            // Non-leaf collection — Any/All семантика не реализована (хвост Z).\n'
        '            if (IsCollectionType(navProp.PropertyType))\n'
        '                throw new NotImplementedException(\$"dot-path through collection navigation not supported: \\"{wireDotPath}\\" (segment \\"{seg}\\" → {navProp.Name} is collection). Use Any/All filter instead.");\n'
        '\n'
        '            current = Expression.Property(current, navProp);\n'
        '            intermediateNavs.Add(current);\n'
        '            currentType = navProp.PropertyType;\n'
        '        }\n'
        '\n'
        '        // Недостижимо — return из loop в isLast-ветке.\n'
        '        throw new InvalidOperationException("unreachable");\n'
        '    }\n'
        '\n'
        '    /// <summary>Строит AND-цепочку null-check для всех intermediate nav-expressions.\n'
        '    /// Возвращает null если intermediate-сегментов нет (1-сегментный dot-path —\n'
        '    /// теоретически невозможен, но защитно).\n'
        '    ///\n'
        '    /// Мотивация (не удалять!): null-guard для intermediate navs предотвращает NRE на\n'
        '    /// InMemory LINQ когда промежуточная nav-property = null; EF Core+Postgres сам делает\n'
        '    /// LEFT JOIN с NULL-propagation семантикой (\$"x.Nav.Field == val\\" на NULL row даёт\n'
        '    /// NULL → false), но мы должны быть провайдер-нейтральны. На Postgres избыточный\n'
        '    /// null-check EF Core устраняет при компиляции в SQL (constant folding). Семантическая\n'
        '    /// эквивалентность с/без guard на relational — гарантирована (NULL-propagation).</summary>\n'
        '    private static Expression? BuildNullGuard(List<Expression> navs)\n'
        '    {\n'
        '        if (navs.Count == 0) return null;\n'
        '        Expression acc = Expression.NotEqual(navs[0], Expression.Constant(null, navs[0].Type));\n'
        '        for (int i = 1; i < navs.Count; i++)\n'
        '            acc = Expression.AndAlso(acc, Expression.NotEqual(navs[i], Expression.Constant(null, navs[i].Type)));\n'
        '        return acc;\n'
        '    }\n'
        '\n'
        '    /// <summary>Резолвит nav-property для FK-сегмента.\n'
        '    /// Primary: EF Core IModel — `entityType.GetNavigations()` matching по\n'
        '    /// `INavigation.ForeignKey.Properties[0].Name == csharpFkName`.\n'
        '    /// Fallback: strip "Id"-суффикс с C# property name (нестандартная конфигурация —\n'
        '    /// FK без nav-property; редкий случай, лог не пишем — нет ILogger, см. хвост R).</summary>\n'
        '    private static PropertyInfo ResolveNavProperty(\n'
        '        Type ownerType, string csFkName, IModel efModel, string fullPath, string segment)\n'
        '    {\n'
        '        var efType = efModel.FindEntityType(ownerType);\n'
        '        if (efType is not null)\n'
        '        {\n'
        '            foreach (var nav in efType.GetNavigations())\n'
        '            {\n'
        '                var fkProps = nav.ForeignKey.Properties;\n'
        '                if (fkProps.Count == 1 && fkProps[0].Name == csFkName)\n'
        '                {\n'
        '                    var navProp = ownerType.GetProperty(nav.Name)\n'
        '                        ?? throw new InvalidOperationException(\$"EF nav {nav.Name} on {ownerType.Name} not reflectable as PropertyInfo (path \\"{fullPath}\\")");\n'
        '                    return navProp;\n'
        '                }\n'
        '            }\n'
        '        }\n'
        '        // Fallback: strip "Id" suffix from FK name.\n'
        '        if (csFkName.EndsWith("Id"))\n'
        '        {\n'
        '            var navName = csFkName[..^2];\n'
        '            var navProp = ownerType.GetProperty(navName);\n'
        '            if (navProp is not null) return navProp;\n'
        '        }\n'
        '        throw new InvalidOperationException(\n'
        '            \$"Cannot resolve navigation for FK \\"{csFkName}\\" on {ownerType.Name} " +\n'
        '            \$"(wire path \\"{fullPath}\\", segment \\"{segment}\\"): no IEntityType.GetNavigations match " +\n'
        '            \$"and strip-Id fallback didn\'t find property. Configure HasOne(...).WithMany().HasForeignKey({csFkName}) in EF.");\n'
        '    }\n'
        '\n'
        '    /// <summary>5.6.2: резолвит wire dot-path в C# nav-path string для EF Core `Include(string)`.\n'
        '    /// Каждый сегмент трактуется как navigation (intermediate ИЛИ leaf — Include грузит nav).\n'
        '    /// Wire `"shopId.photoId"` → C# `"Shop.Photo"`. Wire `"teamTable"` (collection в leaf-\n'
        '    /// позиции) → C# `"TeamTable"` (валидно для Include).\n'
        '    /// Отличие от Resolve (для filter/sort): leaf-collection ЗДЕСЬ разрешён (Include грузит\n'
        '    /// коллекцию-табчасть). Non-leaf collection — throw, как в Resolve (хвост Z).</summary>\n'
        '    public static string ResolveAsNavPath(Type rootEntityType, string wireDotPath, IModel efModel)\n'
        '    {\n'
        '        var segments = wireDotPath.Split(\'.\');\n'
        '        if (segments.Length == 0 || segments.Any(string.IsNullOrEmpty))\n'
        '            throw new InvalidOperationException(\$"Invalid wire dot-path \\"{wireDotPath}\\" for ReadReferences: empty segments");\n'
        '\n'
        '        Type currentType = rootEntityType;\n'
        '        var navPath = new StringBuilder();\n'
        '        for (int i = 0; i < segments.Length; i++)\n'
        '        {\n'
        '            var seg = segments[i];\n'
        '            var isLast = i == segments.Length - 1;\n'
        '\n'
        '            if (!TypeFieldMapRegistry.ByType.TryGetValue(currentType, out var maps))\n'

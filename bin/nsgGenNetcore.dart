    sb.writeln();
    sb.writeln('namespace $ns.Controllers;');
    sb.writeln();
    sb.writeln('[ApiController]');
    sb.writeln('[Route("$route")]');
    sb.writeln('public partial class ${t}Controller : ControllerBase');
    sb.writeln('{');
    sb.writeln('    private readonly AppDbContext _db;');
    sb.writeln('    public ${t}Controller(AppDbContext db) { _db = db; }');
    sb.writeln();

    // Read
    if (method.allowGetter) {
      sb.writeln('    /// <summary>POST $route — read with NsgDataRequestParams body.</summary>');
      sb.writeln('    [HttpPost]');
      sb.writeln(
          '    public async Task<ActionResult<List<Dictionary<string, object?>>>> Read([FromBody] JsonElement body)');
      sb.writeln('    {');
